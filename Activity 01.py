#Print significa escreva

print('Olá, mundo!') #Para mostrar uma mensagen deve-se usar aspas simples '' ou aspas duplas "".

print(7+5) #Para mostrar números não é necessário aspas. Note que escrito dessa forma será mostrado o resultado da operação.

print('7'+'5') #Note que escrito dessa forma será mostrado 7 e o 4 junstos, porque aqui o simbolo de mais + é apara junção de elementos.

print ('Olá', '5') #A vírgula também é usada para junção de elementos.
